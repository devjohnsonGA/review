var calculator = {
    add: function(a, b){
       return a + b
    },
    subtract: function(a, b){
        return a - b
     },
     multiply: function(a, b){
        return a * b
     },
     divide: function(a, b){
        return a/b
     }
}

console.log(calculator.add(1, 3))
console.log(calculator.subtract(9, 4))
console.log(calculator.multiply(2, 3))
console.log(calculator.divide(21, 3))
